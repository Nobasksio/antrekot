function getZone(lat, long) {
  let zone = null;
  for (let i = 0; i < coordinates.features.length; i += 1) {
    zone = {
      result: null,
      coords: coordinates.features[i].geometry.coordinates[0],
      description: JSON.parse(JSON.stringify(coordinates.features[i].properties.description)),
    };
    let x = lat;
    let y = long;
    for (let j = 1; j < zone.coords.length; j += 1) {
      let xp = zone.coords[j][1];
      let yp = zone.coords[j][0];
      let xPrev = zone.coords[j-1][1];
      let yPrev = zone.coords[j-1][0];
      if (((yp <= y && y < yPrev) || (yPrev <= y && y < yp)) && (x > (xPrev - xp) * (y - yp) / (yPrev - yp) + xp)) {
        zone.result = true;
        return zone;
      } else {
        zone.result = false;
      }
    }
  }


  return zone;
};

